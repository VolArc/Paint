void PrintImage (char board[17][17]){
    system (CLEAR);
    for (int i = 0; i < 17; i ++){
        for (int j = 0; j < 17; j ++){
            std::cout << board[i][j];
        }
        std::cout << "\n";
    }
    PAUSE;
}

void Fill (uint x, uint y, char board[17][17]) {
    char c = board[y][x];
    if (c != '#' && c != '@'){
        board[y][x] = '@';
        PrintImage(board);
        Fill (x - 1, y, board);
        Fill (x + 1, y, board);
        Fill (x, y - 1, board);
        Fill (x, y + 1, board);
    }
}

